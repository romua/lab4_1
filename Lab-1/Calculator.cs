using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;

namespace Lab_1
{
    public class Calculator
    {
        public static double numAdd(double firstNumber, double secondNumber)
        {
            return firstNumber + secondNumber;
        }

        public static double numSubtract(double firstNumber, double secondNumber)
        {
            return firstNumber - secondNumber;
        }

        public static double numDivide(double firstNumber, double secondNumber)
        {
            return firstNumber / secondNumber;
        }

        public static double numMultiply(double firstNumber, double secondNumber)
        {
            return firstNumber * secondNumber;
        }

       
    }
}